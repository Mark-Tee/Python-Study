"""练习 10 — 列表操作 + 列表推导式

题目 A — 列表增删改查：
  1. 创建列表：['张三', '李四', '王五']
  2. 在第 2 位插入 '赵六'
  3. 删除 '李四'
  4. 修改最后一个人为 '钱七'
  5. 遍历输出所有名字
  6. 排序后输出

题目 B — 列表推导式：
  用列表推导式生成 [1, 3, 5, 7, 9]
"""

# === A. 列表增删改查 ===
students = ['张三', '李四', '王五']

students.insert(1, '赵六')             # ['张三', '赵六', '李四', '王五']
students.remove('李四')                # ['张三', '赵六', '王五']
students[-1] = '钱七'                  # ['张三', '赵六', '钱七']

print("遍历所有学生：")
for name in students:
    print(f"  {name}")

students.sort()                        # 排序（中文按拼音）
print(f"排序后：{students}")

# === B. 列表推导式 ===
odd_numbers = [i for i in range(1, 10) if i % 2 != 0]
print(f"列表推导式生成奇数：{odd_numbers}")  # → [1, 3, 5, 7, 9]
