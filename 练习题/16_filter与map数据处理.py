# ==================== 概念速查 ====================

"""
filter: 从列表中「筛」出符合条件的元素
    filter(判断函数, 可迭代对象)  →  返回迭代器
    判断函数返回 True → 保留，返回 False → 丢弃

    filter(lambda x: x > 10, [5, 15, 8, 20])
    # 挨个判断：5>10? False 丢 | 15>10? True 留 | 8>10? False 丢 | 20>10? True 留
    # → [15, 20]

map: 对列表中每个元素「做变换」
    map(变换函数, 可迭代对象)  →  返回迭代器
    变换函数的结果就是新元素

    map(lambda x: x * 2, [1, 2, 3])
    # 挨个变换：1→2 | 2→4 | 3→6
    # → [2, 4, 6]

列表推导式: 筛选 + 变换一步到位
    [表达式 for 变量 in 列表 if 条件]

    [x*2 for x in [1, 2, 3] if x > 1]
    # 先筛：1 丢 | 2 留 | 3 留
    # 再变：2→4 | 3→6
    # → [4, 6]
"""
